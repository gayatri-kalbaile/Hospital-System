		<div class="row">
			<div class="col-md-6">
				<p style="color: white; font-size: 18px;">
					Medical professionals use computed tomography, also known as CT scan, to examine structures inside your body. A CT scan uses X-rays and computers to produce images of a cross-section of your body. It takes pictures that show very thin “slices” of your bones, muscles, organs and blood vessels so that healthcare providers can see your body in great detail.
				</p>
				<br>
				<br>
				<span style="font-size: 18px;">Typically, you should plan for an hour for a CT scan. Most of that time is for preparation. The scan itself takes between 10 and 30 minutes or less. Generally, you can resume your activities after a healthcare provider says it is safe to do so — usually after they complete the scan and verify clear images.</span>
				<br>
				
			</div>
			<div class="col-md-6">
				<img src="images\ctscan.jpg" height="350px" width="550px">
			</div>
		</div>
		<br>
		<h3 style="color: white">What happens during the test?</h3>
		<br>
		<span style="font-size: 18px;">During the test, you will lie on your back on a table (like a bed). If your test requires it, a healthcare provider may inject the contrast dye intravenously (into your vein). This dye can make you feel flushed or have a metallic taste in your mouth.</span>
		<br>
		<span style="font-size: 18px;">When the scan begins:</span>
		<ul>
			<li style="color: white">The bed slowly moves into the doughnut-shaped scanner. At this point, you will need to stay as still as possible because movement can create blurry images.</li>
			<li style="color: white">The scanner takes pictures of the area the healthcare provider needs to see. Unlike an MRI scan, a CT scan is silent.</li>
			<li style="color: white">When the exam is over, the table moves back out of the scanner.</li>
		</ul>
	</div>
</div>

